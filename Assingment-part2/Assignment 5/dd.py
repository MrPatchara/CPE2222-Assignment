try:
    # ลองสร้างลิสต์ขนาดใหญ่ด้วยการคูณทีละขั้นเพื่อใช้หน่วยความจำมากขึ้น
    large_data = []
    for _ in range(1000000):  # วนลูปเพื่อเพิ่มข้อมูลจำนวนมากทีละขั้น
        large_data.append([0] * 1000000)  # สร้างลิสต์ขนาดใหญ่ภายในลิสต์อีกที
except MemoryError as e:
    print("เกิดข้อผิดพลาดทางหน่วยความจำ:", e)
    print("ไม่สามารถจัดสรรหน่วยความจำเพิ่มเติมได้")
else:
    print("จัดสรรหน่วยความจำสำเร็จโดยไม่มีข้อผิดพลาดทางหน่วยความจำ")

