# รับอินพุตโค้ดจากผู้ใช้
user_input = input("กรุณาป้อนโค้ด Python เพื่อทดสอบ: ")

try:
    # พยายามรันโค้ดที่ผู้ใช้ป้อน
    exec(user_input)
except SyntaxError as e:
    print("เกิดข้อผิดพลาดทางไวยากรณ์:", e)
    print("โปรดตรวจสอบและแก้ไขโค้ด Python ของคุณ")
else:
    print("โค้ดทำงานได้โดยไม่มีข้อผิดพลาดทางไวยากรณ์")

