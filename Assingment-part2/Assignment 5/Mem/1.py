# รับข้อมูลจากผู้ใช้
user_input = input("กรุณาป้อนตัวเลข: ")

try:
    # พยายามบวกข้อมูลที่ผู้ใช้ป้อนกับจำนวนเต็ม 
    result = int(user_input) + "Hello"  # การบวกตัวเลขกับข้อความทำให้เกิด TypeError
except TypeError as e: # เรียกใช้ Exception ที่เป็นซับคลาสของ BaseException โดยใช้ชื่อตัวแปร e เพื่อเก็บข้อมูลข้อผิดพลาด
    print("เกิดข้อผิดพลาดประเภทข้อมูล:", e) 
    print("ไม่สามารถดำเนินการบวกระหว่างประเภทข้อมูลที่ต่างกันได้")
else:
    print("ผลลัพธ์การคำนวณ:", result)

# โค้ดด้านล่างนี้ทำงานปกติหากไม่มีข้อผิดพลาด
print("โปรแกรมดำเนินการต่อ")
